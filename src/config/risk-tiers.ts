export function parseRiskTierNumber(value: unknown): RiskTierNumber | null {
  const parsed = RiskTierNumberSchema.safeParse(value);
  return parsed.success ? parsed.data : null;
}

export function isAggressiveTier(tier: number): boolean {
  return tier >= 7;
}

export function isConservativeTier(tier: number): boolean {
  return tier >= 1 && tier <= 3;
}

export function isActiveDlmmTier(tier: number): boolean {
  return tier >= 6;
}
