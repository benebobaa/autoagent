import { z } from 'zod';

export const RiskTierLabelSchema = z.enum([
  'ultra_conservative',
  'conservative',
  'stable_yield',
  'moderate',
  'balanced',
  'growth',
  'aggressive',
  'degen',
  'ultra_degen',
]);

export type RiskTierLabel = z.infer<typeof RiskTierLabelSchema>;

export const RiskTierNumberSchema = z.union([
  z.literal(1),
  z.literal(2),
  z.literal(3),
  z.literal(4),
  z.literal(5),
  z.literal(6),
  z.literal(7),
  z.literal(8),
  z.literal(9),
]);

export type RiskTierNumber = z.infer<typeof RiskTierNumberSchema>;

export const MeteoraPoolTypeSchema = z.enum(['stable', 'bluechip', 'volatile', 'memecoin']);
export const MeteoraPositionStyleSchema = z.enum(['two_sided', 'one_sided_sol', 'bid_ask']);
export const MeteoraRangeTypeSchema = z.enum(['wide', 'medium', 'tight', 'ultra_tight']);
export const MeteoraDepositTokenSchema = z.enum(['both', 'sol', 'usdc']);

export const TierConfigSchema = z.object({
  tier: RiskTierNumberSchema,
  label: RiskTierLabelSchema,
  capital_allocation_pct: z.number().min(0).max(1),
  max_position_size_usd: z.number().positive(),
  min_position_size_usd: z.number().positive(),
  max_concurrent_positions: z.number().int().positive(),
  allowed_protocols: z.array(z.string()).min(1),
  preferred_protocols: z.array(z.string()).min(1),
  meteora_pool_types: z.array(MeteoraPoolTypeSchema),
  meteora_position_style: MeteoraPositionStyleSchema,
  meteora_bin_step: z.number().int().min(1).max(200),
  meteora_total_bins: z.number().int().min(20).max(70),
  meteora_range_type: MeteoraRangeTypeSchema,
  meteora_deposit_token: MeteoraDepositTokenSchema,
  min_pool_tvl_usd: z.number().nonnegative(),
  max_pool_tvl_usd: z.number().positive(),
  min_volume_24h_usd: z.number().nonnegative(),
  require_volume_spike: z.boolean(),
  volume_spike_multiplier: z.number().positive(),
  require_price_momentum: z.boolean(),
  target_apy_pct: z.number().nonnegative(),
  take_profit_pct: z.number(),
  stop_loss_pct: z.number(),
  max_hold_hours: z.number().positive(),
  check_interval_minutes: z.number().int().positive(),
  max_il_tolerance_pct: z.number().nonnegative(),
  min_fee_apr_pct: z.number().nonnegative(),
  require_human_approval: z.boolean(),
  emergency_exit_on_rug: z.boolean(),
  score_weight_apy: z.number().min(0).max(1),
  score_weight_volume: z.number().min(0).max(1),
  score_weight_tvl: z.number().min(0).max(1),
  score_weight_momentum: z.number().min(0).max(1),
  score_weight_il_risk: z.number().min(0).max(1),
}).superRefine((value, ctx) => {
  const totalWeight =
    value.score_weight_apy +
    value.score_weight_volume +
    value.score_weight_tvl +
    value.score_weight_momentum +
    value.score_weight_il_risk;

  if (Math.abs(totalWeight - 1) > 0.001) {
    ctx.addIssue({
      code: z.ZodIssueCode.custom,
      path: ['score_weight_apy'],
      message: `Tier ${value.tier} score weights must sum to 1.0, got ${totalWeight.toFixed(3)}`,
    });
  }
});

